    private void jsQuantiteStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jsQuantiteStateChanged

        int qt = (Integer) jsQuantite.getValue();
        Livre livre = (Livre) jcbLivre.getSelectedItem();

        int qtLim = Integer.parseInt(livre.getAvailableStock());
        if (qtLim < qt) {
            JOptionPane.showMessageDialog(rootPane, "Le stock disponible n'est pas suffisant", "Stock", JOptionPane.INFORMATION_MESSAGE);
        } else {

            float prixTot = qt * Float.parseFloat(livre.getPrice());
            prixTot = prixTot * 100;
            prixTot = Math.round(prixTot);
            prixTot = prixTot / 100;
            jlPrixTotal.setText(prixTot + " €");
        }
    }//GEN-LAST:event_jsQuantiteStateChanged

