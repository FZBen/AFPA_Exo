    private DefaultComboBoxModel initLivre() {
        Vector v = new Vector();

        Xchange ech = new Xchange();
        Statement stat = null;
        String query = "SELECT BOOKISBN13,BOOKTITLE,BOOKPRICE, BOOKAVAILABLESTOCK FROM LIVRE WHERE STATUSID != 499";
        ResultSet rs = null;

        try {

            ech.connect();
            stat = ech.getConnexion().createStatement();
            rs = stat.executeQuery(query);

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Création connexion Livre: " + ex.getErrorCode() + " / " + ex.getMessage(), "LigneCMDMod", JOptionPane.ERROR_MESSAGE);;
        }
        try {
            while (rs.next()) {
                Livre livre;
                livre = new Livre() {
                    @Override
                    public String toString() {
                        return getIsbn13() + " : " + getTitle() + " : " + getPrice() + " €, Stock : " + getAvailableStock();
                    }
                };
                livre.setIsbn13(rs.getString("BOOKISBN13"));
                livre.setPrice(rs.getString("BOOKPRICE"));
                livre.setTitle(rs.getString("BOOKTITLE"));
                livre.setAvailableStock(rs.getString("BOOKAVAILABLESTOCK"));

                v.add(livre);

            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Lecture données Livre : " + ex.getErrorCode() + " / " + ex.getMessage(), "LigneCMDMod", JOptionPane.ERROR_MESSAGE);
        } catch (DataExceptions ex) {
            JOptionPane.showMessageDialog(null, "Lecture données Livre : " + ex.getErrorCode() + " / " + ex.getMessage(), "LigneCMDMod", JOptionPane.ERROR_MESSAGE);
        } finally {
            ech.close();
        }

        return new DefaultComboBoxModel(v);
    }

