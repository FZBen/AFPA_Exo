    private void jbValiderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbValiderActionPerformed

        LigneCmd ligne = new LigneCmd();
        ligne.setClient(client);
        ligne.setCommande(commande);
        System.out.println("ID commande = " + commande.getID());
        Livre livre = (Livre) jcbLivre.getSelectedItem();
        ligne.setLivre(livre);
        int qt = (Integer) jsQuantite.getValue();
        int error = 0;
        try {
            ligne.setQuantite(String.valueOf(qt));
        } catch (DataExceptions ex) {
            JOptionPane.showMessageDialog(rootPane, "CréationLigneCmd :" + ex.getMessage(), "Ligne de Commande", JOptionPane.INFORMATION_MESSAGE);
            error += 1;
        }
        try {
            ligne.setTva("5.5");
        } catch (DataExceptions ex) {
            JOptionPane.showMessageDialog(rootPane, "CréationLigneCmd :" + ex.getMessage(), "Ligne de Commande", JOptionPane.INFORMATION_MESSAGE);
            error += 1;
        }
        try {
            ligne.setPrixHT(livre.getPrice());
        } catch (DataExceptions ex) {
            JOptionPane.showMessageDialog(rootPane, "CréationLigneCmd :" + ex.getMessage(), "Ligne de Commande", JOptionPane.INFORMATION_MESSAGE);
            error += 1;
        }

        if (error == 0) {
            Xchange ech = new Xchange();
            ech.connect();
            ligne.save(ech, ligne.genValue());
            ech.close();
            jfMere.refresh();

        }

        this.dispose();

    }//GEN-LAST:event_jbValiderActionPerformed

