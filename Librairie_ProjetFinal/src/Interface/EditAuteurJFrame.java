    private void editjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editjButtonActionPerformed
        // TODO add your handling code here:
        Pattern p01 = Pattern.compile("[- '√Ä-»≥-a-zA-Z]{1,100}");
        Matcher m01 = p01.matcher(lastNamejTextField.getText());
        Matcher m02 = p01.matcher(firstNamejTextField.getText());

        if (m01.matches() && m02.matches()) {

            Xchange xch = new Xchange();
            xch.connect();

            PreparedStatement ps;

            try {
                String query = "UPDATE AUTEUR "
                + "SET STATUSID = ?, AUTHORLASTNAME = ?, AUTHORFIRSTNAME = ?, AUTHORBIO = ?, AUTHORPICTURE = ?, AUTHORNOTES = ? "
                + "WHERE AUTHORID = ?";
                ps = xch.getConnexion().prepareStatement(query);
                Statut status = (Statut) StatusjComboBox.getSelectedItem();
                ps.setString(1, status.getId().toString());
                ps.setString(2, lastNamejTextField.getText());
                ps.setString(3, firstNamejTextField.getText());
                ps.setString(4, biojTextArea.getText());
                if (imagePth == null)
                ps.setNull(5, java.sql.Types.BLOB);
                else {
                    InputStream img = new FileInputStream(new File(imagePth));
                    ps.setBlob(5, img);
                }
                ps.setString(6, notesjTextArea.getText());
                ps.setString(7, IDjTextField.getText());

                if (ps.executeUpdate() != 0){
                    JOptionPane.showMessageDialog(null, "Auteur modifi√©");
                    MotherJFrame.refreshTable();
                    this.dispose();
                    xch.close();

                } else {
                    JOptionPane.showMessageDialog(null, "Erreur");
                    xch.close();
                }

            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(null, "Champ(s) vide(s) ou caract√®res non valides", "Erreur de saisie", JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_editjButtonActionPerformed
    
